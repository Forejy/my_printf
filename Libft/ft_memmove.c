#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	char		*d;
	const char	*s;
	size_t		i;

	d = (char *)dest;
	s = (const char *)src;
	i = n;
	if (s < d)
	{
		while (i--)
			d[i] = s[i];
	}
	else
		ft_memcpy(d, s, n);
	return (d);
}
